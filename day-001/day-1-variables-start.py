# This covered on learning how varibles work and how they work in line and can be changed.
# Here we have two varibles first one is named Jack and then prints the string to the console.
name = "Jack"
print(name)
# Here we have another varible named name and then we have changed the varible to the string Angela. Which if run it will then print out Jack as the first name and Angela as the second name on the next line.
name = "Angela"
print(name)

# Finally we have the name is set as an input following by the len function which will then print out the length of the name. Which is then called in the print function.
name = input("What is your name?")
length = len(name)
print(length)